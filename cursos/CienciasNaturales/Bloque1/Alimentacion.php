							<b>Aparato digestivo</b>

							<br>Las arterias, las venas y el corazón, forman un circuito. El corazón es un órgano que se encuentra en la parte media del tórax y se encarga de bombear la sangre para que llegue a todo el cuerpo. Las arterias llevan la sangre del corazón al resto del cuerpo con las sustancias que este requiere, mientras que las venas llevan la sangre del cuerpo al corazón, con algunas sustancias de desecho. El corazón, las venas y las arterias forman pate del aparato circulatorio.</p> 
							<p><b>Aparato circulatorio</b><br>
							La sangre, además de transportar los nutrimentos, también transporta oxígeno. Al respirar utilizamos el aparato respiratorio, que esta compuesto por varios tubos o conductos que se inician en la nariz, se conectan con la boca y continúan en la tráquea, los bronquios y bronquiolos, hasta llegar a una estructuras microscópicas en forma de sacos llamados alveolos, situadas en el interior de los pulmones, donde se lleva a cabo el intercambio de gases: oxígeno y dióxido de carbono. </p>
							<p><b>Aparato respiratorio</b><br>
